%《线性代数实验践及MATLAB入门》第六章例题程序ag603a
% 行阶梯形式变换的小分解动作
% 电子工业出版社出版  陈怀琛 龚杰民合著 2005年10月
%
A=[1,4,7;8,5,2;3,6,-2]
b=[1;3;5]
C=[A,b]
C1=gauss(C,1,2,1)
C1=gauss(C1,1,3,1)
C1=gauss(C1,2,3,2)
disp('已变换为行阶梯形式'),pause
C1=gauss(C1,3,2,3)
C1=gauss(C1,3,1,3)
C1=gauss(C1,2,1,2)
disp('已把行阶梯形式变换为对角型'),pause
C1(2,:)=-1/27* C1(2,:)
C1(3,:)=-1/11* C1(3,:)
disp('已变换为简化行阶梯形式')
